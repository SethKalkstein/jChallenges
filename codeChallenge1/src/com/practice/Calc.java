package com.practice;

public class Calc {

    public int add(int a, int b){
        return a + b;
    }

    public int sub(int a, int b){
        return a -b;
    }

    public int mul(int a, int b) {
        return a * b;
    }

    public float div(float a, float b){
        float result = a / b;
        return result;
    }
}
